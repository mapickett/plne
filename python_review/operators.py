# Arithmatic Operators

# +
print(5 + 5)

# -
print(8 - 3)

# /
print(8 / 4) # 2.0

# // integer division
print(11 // 5)

# *
print(5 * 4)

# ** power
print(2 ** 3)

# % mod
print(8 % 5)

'''
Order of Operations
1 - Exponential
2 - Multiplication / Division
3 - Addition / Subtraction

Use parenthesis to modify order
'''

