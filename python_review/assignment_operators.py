# Equals(=)

a = 5

# Plus equals(+=)

a += 2

print(a)

# Minus Equals(-=)
a -= 3
print(a)

# Star equals(*=)
a *= 3
print(a)

# Slash equals(/=)
a /= 2
print(a)

# double star equals(**=)
a **=3
print(a)

# percent equals(%=)
a %= 5
print(a)

# divmod
a, b = divmod(14, 6)
print(a, b) 

# pow()
print(pow(5, 9))

# sum()
print(sum([1,2,5,6,7,9]))

# max()
print(max([1,2,5,6,7,9]))

# min()
print(min([1,2,5,6,7,9]))

# round()
a = 5.666772
print(round(a, 3))

b = round(a, 1)
print(a, b)
